/// 앱 공통 컴포넌트 사이즈 토큰
///
/// 버튼, 아이콘 버튼, 세그먼티드 컨트롤 등 small/medium/large 스케일을
/// 공유하는 컴포넌트의 높이 및 아이콘 크기를 정의한다.
class AppSize {
  AppSize._();

  /// 컴포넌트 높이/지름 (small)
  static const double componentSmall = 36.0;

  /// 컴포넌트 높이/지름 (medium)
  static const double componentMedium = 44.0;

  /// 컴포넌트 높이/지름 (large)
  static const double componentLarge = 52.0;

  /// 아이콘 크기 (small)
  static const double iconSmall = 18.0;

  /// 아이콘 크기 (medium)
  static const double iconMedium = 20.0;

  /// 아이콘 크기 (large)
  static const double iconLarge = 24.0;
}
